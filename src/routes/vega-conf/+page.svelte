	<div class="lead">Vega/Altair Conference &mdash; Sep 18-19, 2023</div>
	<div class="para">
		The University of Washington, Seattle will host an in-person <strong
			>Vega/Altair Conference</strong
		>, September 18-19!'
	</div>
	<div class="para">
		The event will focus on the development of the <a class="b" href="https://vega.github.io/vega"
			>Vega</a
		>, <a class="b" href="https://vega.github.io/vega-lite">Vega-Lite</a>, and
		<a class="b" href="https://altair-viz.github.io/">Altair</a>
		languages, ecosystem, and corresponding tools, documentation, and learning resources. ' The
		conference will include project talks, roadmap/governance discussions, working groups, and
		social events. ' The event is <em>not</em> intended to be a more general "user" conference &ndash;
		though we would love to have one in the future!'
	</div>

	<div class="para">
		All attendees are expected to follow the <a
